# include <stdio.h>
int main () {

  // using for loop to print 100 to 50 in descending order
  
  printf("Numbers from 100 to 50 in descending order:\n");
  
 for(int i = 100; i>=50; i--) {
  printf("%d\n", i);
 }
   
  return 0;
} 
 